BMW X5 Prices: BMW retails the X5 from Rs 79.90 to Rs 95.90 lakh (ex-showroom)<br><br>

BMW X5 Variants: It is available in three broad trims: SportX Plus, xLine, and M Sport.<br><br><br>

BMW X5 Engine and Transmission: The X5 is powered by both petrol and diesel engine options. The petrol mill is a 3-litre twin-turbo engine making 340PS and 450Nm. The diesel unit on the other hand, is a 3-litre twin-turbo diesel engine with an output of 265PS and 620Nm. Both are mated to an 8-speed automatic transmission and power is delivered to all four wheels.<br><br>

BMW X5 Features: The carmaker has packed the SUV with a bunch of features such as a 12.3-inch digital instrument cluster, adaptive LED headlights, multifunctional seats with massage function, four-zone climate control, panoramic sunroof, 12.3-inch touchscreen infotainment system and Harman Kardon surround sound system. For added versatility, the X5 is also offered with an optional third row of seats.<br><br>

BMW X5 Safety: Safety kit onboard includes six airbags, ABS with EBD, hill descent control, tyre pressure monitoring system and cornering brake control (CBC). The BMW also packs a 360-degree camera system with semi-autonomous parking assist.<br><br>

BMW X5 Rivals: The BMW X5 is a direct rival to other SUVs such as the Mercedes Benz GLE, Audi Q7, Range Rover Velar and the Volvo XC90.<br><br>

	 
